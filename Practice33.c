// Write a C program to print all natural numbers in reverse (from n to 1). - using while loop
#include <stdio.h>

int main()
{
    int a;

    printf("Enter value of a: ");
    scanf("%d", &a);

    while (a >= 1)
    {
        printf("%d\n", a);
        a--;
    }

    return 0;
}

